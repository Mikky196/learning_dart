void main(List<String> args) {
  const int someInteger= 20; //Integer
  print(someInteger);
  const double someDouble = 20.0; //Floating point or double
  print(someDouble);
  const String someString = 'Hi'; // String
  print(someString);
  const bool someBoolean = true; // Boolean
  print(someBoolean);
  const List<int> someList = [1,2,3]; // List
  print(someList);
  const Map<String, String> someMap = {'name': 'Micheal'}; // Map or dictionary
  print(someMap);
  const Set<int> someSet = {1,2,3}; // Set
  print(someSet);
  const dynamic someNull = null; // Dynamic
  print(someNull);
  const Symbol someSymbol = #someNull; // Symbol
  print(someSymbol);

}