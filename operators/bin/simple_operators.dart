void main(List<String> args) {
  const age1 = 22;
  const age2 = 20;
  print(age1 + age2); // Addition operator
  print(age1 - age2); // Subtraction operator
  print(age1 * age2); // Multoplication operator
  print(age1 / age2); // Division operator
  print(age1 ~/ age2); // Truncating division operator (This removes all the decimal part of the division result)
}